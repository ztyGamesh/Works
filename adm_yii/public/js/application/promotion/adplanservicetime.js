function initAll() {
    //数字按键上鼠标滑过的动画效果
    $('.ui-schedule-time').on('mouseover', function () {
        // console.log('over')
        $(this).addClass('ui-schedule-time-hover');

    });
    $('.ui-schedule-time').on('mouseleave', function () {
        // console.log('leave')
        $(this).removeClass('ui-schedule-time-hover');
    });

    //    数字按键点击选择效果
    $('.ui-schedule-time').on('click',  function () {
        var dayId = $(this).attr('day');
        var timeId = $(this).attr('time');
        if($(this).hasClass('ui-schedule-time-selected')){
            $(this).removeClass('ui-schedule-time-selected');
            $(".ui-schedule-timehead[time='" + timeId + "']").removeClass('ui-schedule-timehead-active');
            $("#ctrlschedulemyScheduleGrouplineState"+ dayId).attr('checked', false);
        }else {
            $(this).addClass('ui-schedule-time-selected');
            //            先判断箭头
            if(!$(".ui-schedule-timehead[time='" + timeId + "']").hasClass('ui-schedule-timehead-active')){
                var length = $(".ui-schedule-time-selected[time='" + timeId + "']").length;
                if(length > 6){
                    $(".ui-schedule-timehead[time='" + timeId + "']").addClass('ui-schedule-timehead-active');
                }
            }
            //          再判断日期一排
            if(!$("#ctrlschedulemyScheduleGrouplineState"+ dayId).is(':checked')){
                var length = $(".ui-schedule-time-selected[day='" + dayId + "']").length;
                if(length == 24){
                    $("#ctrlschedulemyScheduleGrouplineState"+ dayId).prop('checked', true);
                }
            }
        }
    })

    //    列箭头选项点击效果
    $(".ui-schedule-timehead").on('click', function () {
        var timeId =  $(this).attr('time');

        if($(this).hasClass('ui-schedule-timehead-active')){
            $(this).removeClass('ui-schedule-timehead-active');
            $("div[time='" + timeId + "']").removeClass('ui-schedule-time-selected');
            $(".ui-schedule-day input").attr('checked',false);
        }else {
            $(this).addClass('ui-schedule-timehead-active');
            //            var timeId =  $(this).attr('time');
            $("div[time='" + timeId + "']").addClass('ui-schedule-time-selected');
            for(var i = 0; i < 7; i++){
                var length = $(".ui-schedule-time-selected[day='" + i + "']").length;
                if(length == 24){
                    $("#ctrlschedulemyScheduleGrouplineState"+ i).prop('checked', true);
                }

            }

        }
    });
    //    星期复选框点击效果
    $(".ui-schedule-day input").on('click', function () {
        var dayId =  $(this).val();
        var length = $(".ui-schedule-day input[type='checkbox']:checked").length;
        if($(this).is(':checked')){
            $("div[day='" + dayId + "']").addClass('ui-schedule-time-selected');
            if(length == 7){
                $(".ui-schedule-timehead").addClass('ui-schedule-timehead-active');
            }
        }else {
            //            var dayId =  $(this).val();
            $("div[day='" + dayId + "']").removeClass('ui-schedule-time-selected');
            $(".ui-schedule-timehead").removeClass('ui-schedule-timehead-active');
        }
    });
    $(".allTime").on('click', allTime);
    $(".workDay").on('click', workDay);
    $(".weekendDay").on('click', weekendDay);

    function allTime() {
        $(".ui-schedule-timehead").addClass('ui-schedule-timehead-active');
        $(".ui-schedule-day input").prop('checked', true);
        $('.ui-schedule-time').addClass('ui-schedule-time-selected');
    }
    function workDay() {
        $(".ui-schedule-timehead").removeClass('ui-schedule-timehead-active');

        for(var i = 0; i < 5; i++){
            $("#ctrlschedulemyScheduleGrouplineState"+ i).prop('checked', true);
            $("div[day='" + i + "']").addClass('ui-schedule-time-selected');

        }
        for(var j = 5; j < 7; j++){
            $("#ctrlschedulemyScheduleGrouplineState"+ j).prop('checked', false);
            $("div[day='" + j + "']").removeClass('ui-schedule-time-selected');
        }
    }
    function  weekendDay() {
        $(".ui-schedule-timehead").removeClass('ui-schedule-timehead-active');

        for(var i = 5; i < 7; i++){
            $("#ctrlschedulemyScheduleGrouplineState"+ i).prop('checked', true);
            $("div[day='" + i + "']").addClass('ui-schedule-time-selected');
        }
        for(var j = 0; j < 5; j++){
            $("#ctrlschedulemyScheduleGrouplineState"+ j).prop('checked', false);
            $("div[day='" + j + "']").removeClass('ui-schedule-time-selected');
        }
    }


};

function notAllTime() {
    $(".ui-schedule-timehead").removeClass('ui-schedule-timehead-active');
    $(".ui-schedule-day input").attr('checked', false);
    $('.ui-schedule-time').removeClass('ui-schedule-time-selected');
}

